class Arithmetic {
  double add({double firstNumber = 0.0, double secondNumber = 0.0}) =>
      firstNumber + secondNumber;

  double subtract({double firstNumber = 0.0, double secondNumber = 0.0}) =>
      firstNumber - secondNumber;

  double multiply({double firstNumber = 0, double secondNumber = 0}) =>
      firstNumber * secondNumber;
    
   double divide({double firstNumber = 1, double secondNumber = 1}) =>
      firstNumber / secondNumber;
}
